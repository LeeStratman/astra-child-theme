// Setup autoloader (via Composer or custom).
if ( file_exists( get_stylesheet_directory() . '/vendor/autoload.php' ) ) {
	require get_stylesheet_directory() . '/vendor/autoload.php';
} else {
	/**
	 * Custom autoloader function for theme classes.
	 *
	 * @access private
	 *
	 * @param string $class_name Class name to load.
	 * @return bool True if the class was loaded, false otherwise.
	 */
	function _swd_astra_child_autoload( $class_name ) {
		$namespace = 'SWD\Astra_Child';

		if ( strpos( $class_name, $namespace . '\\' ) !== 0 ) {
			return false;
		}

		$parts = explode( '\\', substr( $class_name, strlen( $namespace . '\\' ) ) );

		$path = get_stylesheet_directory() . '/inc';
		foreach ( $parts as $part ) {
			$path .= '/' . $part;
		}
		$path .= '.php';

		if ( ! file_exists( $path ) ) {
			return false;
		}

		require_once $path;

		return true;
	}
	spl_autoload_register( '_swd_astra_child_autoload' );
}

require get_stylesheet_directory() . '/inc/showcase.php';
